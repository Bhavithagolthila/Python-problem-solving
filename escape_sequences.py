# Escape Sequence Characters in Python

# New line
print("Hello\nWorld")

# Tab space
print("Hello\tWorld")

# Backslash
print("This is a backslash: \\")

# Single quote inside string
print('I\'m learning Python')

# Double quote inside string
print("He said \"Python is easy\"")

# Carriage return
print("Hello\rWorld")

# Backspace
print("Helloo\b")
