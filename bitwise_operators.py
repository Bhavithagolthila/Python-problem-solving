# Bitwise Operators in Python
# Demonstrates AND, OR, XOR, NOT, Left Shift, Right Shift

a = 5
b = 3

print("a =", a)
print("b =", b)

# Bitwise AND
print("a & b =", a & b)

# Bitwise OR
print("a | b =", a | b)

# Bitwise XOR
print("a ^ b =", a ^ b)

# Bitwise NOT
print("~a =", ~a)

# Left Shift
print("a << 1 =", a << 1)

# Right Shift
print("a >> 1 =", a >> 1)
