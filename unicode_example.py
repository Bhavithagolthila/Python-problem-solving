# Unicode Example in Python

# Convert characters to Unicode values using ord()

a = "A"
b = "a"
c = "Z"

print("Unicode of A:", ord(a))
print("Unicode of a:", ord(b))
print("Unicode of Z:", ord(c))

# Convert Unicode values to characters using chr()

x = 65
y = 66

print("Character of 65:", chr(x))
print("Character of 66:", chr(y))
